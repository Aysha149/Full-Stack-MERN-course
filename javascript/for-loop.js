// for-loop 1 to 10
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

// for-loop 1 to 10 in one line 
let str = "";
for (let i = 1; i <= 10; i++) {
  str = str + i + " ";
  }

console.log(str);

// for-loop 1 to 10 reverse
for (let i = 10; i >= 0; i--) {
  console.log(i);
}

// pyramid
let py = "";        
for (let i = 1; i <= 10; i++) {
  py = py + i + " ";
  console.log(py);
}










